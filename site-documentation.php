<p><strong>Family, linked! This is Familink!</strong><p>

                        <p>A family is the heart of any person's social group. And as much as we would like to,
                            keeping in touch and organizing events with all of our family members can be difficult.
                            People move. Contact points change. Calendars rarley sync. How do we make sure that heart keeps beating?<p>

                        <p>Enter Familink. With this app you can:<p>
                   
                 

                        <ul >
                            <strong>Reference</strong>
                            <li>-Get up-to-date family member contact info, such as phone numbers, email addresses, and/or physical addresses</li>
                            <li>-Edit that contact info on-the-fly, giving everyone else in the family instant access to those changes from a central location</li>
                            <li>-Get remined of family members' birthdays, anniversaries, and other important upcoming events!</li>
                            <br>
                            <strong>Connect</strong>
                            <li>-At the click of a button, send email to your whole extended family or individual Family Units without the fuss of email lists</li>
                            <li>-Quickly call, text, or email any family member, to their current contact details, through the integrated phone API.</li>        
                            <br>
                            <strong>Organize</strong>
                            <li>-Post custom events or parties, with their details, and have included members get notified when you do!</li>
                            <li>-Organizing that family party or event? Post messages on the open forum and/or reply to messages other family members have posted to keep everyone in sync!</li>   
                            <br>
                            <strong>Archive</strong>
                            <li>-COMING SOON - Export contact info and family events to your phone/computer</li>
                            <li>-COMING SOON - Family stories, recipes, thoughts, or memories to share? Let this app be an archive for those important things!</li>
                            <li>-COMING SOON - With Dropbox integration, quickly access photos from past family events</li>
                            <br>
                            <strong>Secure</strong>
                            <li>-Only family members will ever have access to your family's data.</li>
                        </ul>
